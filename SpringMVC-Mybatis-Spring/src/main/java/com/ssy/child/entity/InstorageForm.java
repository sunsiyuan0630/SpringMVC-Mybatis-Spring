package com.ssy.child.entity;


public class InstorageForm {
	
	private String goodName;
	
	private String goodId;
	
	private String goodSupplier;
	
	private String price;
	
	private String goodSpeci;
	
	private String number;
	
	private Integer people;
	
	private String date;

	
	public InstorageForm(){
		
	}

	public String getGoodName() {
		return goodName;
	}

	public void setGoodName(String goodName) {
		this.goodName = goodName;
	}

	public String getGoodId() {
		return goodId;
	}

	public void setGoodId(String goodId) {
		this.goodId = goodId;
	}

	public String getGoodSupplier() {
		return goodSupplier;
	}

	public void setGoodSupplier(String goodSupplier) {
		this.goodSupplier = goodSupplier;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getGoodSpeci() {
		return goodSpeci;
	}

	public void setGoodSpeci(String goodSpeci) {
		this.goodSpeci = goodSpeci;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	

	public Integer getPeople() {
		return people;
	}

	public void setPeople(Integer people) {
		this.people = people;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}
	
	

}
